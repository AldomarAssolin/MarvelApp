class Characters{
    constructor(id, name, description, imageUrl, modified, comics, stories, series){
        this.id = id;
        this.name = name;
        this.description = description;
        this.imageUrl = imageUrl;
        this.modified = modified;
        this.comics = comics;
        this.stories = stories;
        this.series = series
    }
}

module.exports = Characters